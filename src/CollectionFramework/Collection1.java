//순서를 유지하지 않은 데이터의 집합. 데이터의 중복을 허용하지 않는다. 예)양의 정수집합, 음의 정수집합

//3. Map
//키(key)와 값(Value)의 쌍(pair)으로 이루어진 데이터의 집합. 순서는 유지되지 않으며, 키는 중복을 허용하지 않고, 값은 중복을 허용한다. 예)우편번호, 지역번호
